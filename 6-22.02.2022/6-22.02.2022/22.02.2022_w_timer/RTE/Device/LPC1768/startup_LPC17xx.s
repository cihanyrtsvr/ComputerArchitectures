SysTick_Handler PROC
                PUSH{R0-R3,LR}
				
					LDR R9,=numIteration
					LDR R10,[R9]
					
					CMP R10,#10
					BHI FIN
					
					MOV R11,#4
					MUL R11,R10
					
					LDR R4,=arrayG
					LDR R5,[R4,R11]
					
					
					MOV R0,R5
					MOV R1,#10
					MOV R2,#3
					MOV R3,#0
					BL	computeG
					
					ADD R10,#1
					MOV R11,#4
					MUL R11,R10
					STR R0, [R4,R11]  
					
					STR R10,[R9]
					
FIN				POP{R0-R3,PC}
                ENDP

