; Reset Handler

Reset_Handler   PROC
                EXPORT  Reset_Handler             [WEAK]
                
				LDR R4,X
				LDR R5,N
				LDR R6,Y
				LDR R7,M
				
				
				LDR R4,=arrayG
				MOV R5,#2_1000000
				STR R5,[R4]
				
				LDR R0,ControlSts
				MOV R1,#0
				STR R1,[R0]
				
				LDR R0,ReloadReg
				MOV R1,#250;0X00100000
				STR R1,[R0]
				
				LDR R0,CurrentVal
				MOV R1,#4
				STR R1,[R0]
				
				LDR R0,ControlSts
				MOV R1,#7
				STR R1,[R0]
				
				
STOP			B	STOP
                ENDP


; Dummy Exception Handlers (infinite loops which can be modified)
computeG     PROC
	PUSH {R4-R11,LR}
	
			MOV R4,R0 ;X
			MOV R5,R1 ;N
			
LP_X		AND R9,R4,#1 ;Y_PRE
			CMP R9, #0
			ITTT EQ
			LSREQ R4,#1 ;x
			SUBEQ R5,#1 ;n'
			BEQ LP_X
			BNE NE
			
NE			ADD R10,R5,R5
			MOV R6,R2 ;Y 
			MOV R7,R3
			
LP_Y		AND R9,R6,#1 ;Y_PRE
			CMP R9, #0
			ITTT EQ
			LSREQ R6,#1 ;Y
			SUBEQ R7,#1 ;M
			BEQ LP_Y
			BNE NEXT
			
			

NEXT		MUL  R9,R4,R6
			ADD  R10,R7 ;Q
			
			MOV R11, #3
			
			LSL R11,R10
			SUB R11,R9
			
			MUL R11,R4 
			LSR R11,#1
			ADD R10,R5 ;R
			
			CMP R1,R3
			IT	HI
			SUBHI R4,R10,R1
			SUBLS R4,R10,R3
			
			LSR R0,R11,R4
			
			
	POP	{R4-R11,PC}
                ENDP
					
